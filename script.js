// Add typing effect to hero title
document.addEventListener('DOMContentLoaded', () => {
    const heroTitle = document.querySelector('#hero-title');
    if (heroTitle) {
        const text = 'Smart Resume Analysis with Resume Grok';
        const highlightText = 'Resume Grok';
        heroTitle.innerHTML = '';
        
        let i = 0;
        function typeWriter() {
            if (i < text.length) {
                const char = text.charAt(i);
                if (text.substring(i).startsWith(highlightText)) {
                    heroTitle.innerHTML += `<span class="highlight">${highlightText}</span>`;
                    i += highlightText.length;
                } else {
                    heroTitle.innerHTML += char;
                    i++;
                }
                setTimeout(typeWriter, 50);
            }
        }
        
        // Start typing effect after a short delay
        setTimeout(typeWriter, 500);
    }
});

// Performance optimization: Lazy load images if any are added later
if ('IntersectionObserver' in window) {
    const imageObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const img = entry.target;
                img.src = img.dataset.src;
                img.classList.remove('lazy');
                imageObserver.unobserve(img);
            }
        });
    });
    
    document.querySelectorAll('img[data-src]').forEach(img => {
        imageObserver.observe(img);
    });
}